namespace NBarCodes {

  /// <summary>
  /// Identifies the barcode types supported by the assembly.
  /// </summary>
  public enum BarCodeType {

    /// <summary>
    /// Standard 2 of 5 barcode.
    /// </summary>
    Standard25,

    /// <summary>
    /// Interleaved 2 of 5 barcode.
    /// </summary>
    Interleaved25,

    /// <summary>
    /// Code 3 of 9 barcode.
    /// </summary>
    Code39,

    /// <summary>
    /// Code 128 barcode.
    /// </summary>
    Code128,

    /// <summary>
    /// EAN-8 barcode.
    /// </summary>
    Ean8,

    /// <summary>
    /// EAN-13 barcode.
    /// </summary>
    Ean13,

    /// <summary>
    /// UPCA barcode.
    /// </summary>
    Upca,

    /// <summary>
    /// UPCE barcode.
    /// </summary>
    Upce,

    /// <summary>
    /// Postnet barcode.
    /// </summary>
    PostNet
  }

}
