using System;

namespace NBarCodes {

	/// <summary>
	/// Identifies the unit of measure used in a barcode rendering.
	/// </summary>
	public enum BarCodeUnit {

		/// <summary>
		/// Centimeter unit.
		/// </summary>
		Centimeter = 0,

		/// <summary>
		/// Millimeter unit.
		/// </summary>
		Millimeter = 1,
		
		/// <summary>
		/// Inch unit.
		/// </summary>
		Inch = 2,

		/// <summary>
		/// Pixel unit.
		/// </summary>
		Pixel = 3
	}

}
